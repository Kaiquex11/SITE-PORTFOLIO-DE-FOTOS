function Inicio()
{
    let inicio= document.getElementById("todos")
    let cachorro= document.getElementById("cachorro")
    let gato= document.getElementById("gato")
    let casa= document.getElementById("casa")
    let praia= document.getElementById("praia")
    let fim= document.getElementById("fim")

    gato.classList.add("sumir")
    casa.classList.add("sumir")
    praia.classList.add("sumir")
    fim.classList.add("sumir")
    cachorro.classList.add("sumir")

    inicio.classList.remove("sumir")
}


